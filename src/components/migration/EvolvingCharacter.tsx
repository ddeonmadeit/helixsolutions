const STAGES = [Stage1, Stage2, Stage3, Stage4, Stage5];

// Maps selection count to stage index: 0→0, 1→1, 2→2, 3→3, 4+→4
const getStageIndex = (selectionCount: number) => Math.min(selectionCount, 4);

// Each stage has a different natural aspect ratio; these sizes keep them visually balanced
const STAGE_SIZES = [
  { width: 80, height: 100 },   // Stage 1: compact
  { width: 100, height: 110 },  // Stage 2: slightly wider
  { width: 110, height: 115 },  // Stage 3: wider with arms
  { width: 120, height: 140 },  // Stage 4: taller with spikes
  { width: 130, height: 160 },  // Stage 5: fully evolved
];

interface EvolvingCharacterProps {
  selectionCount: number;
  color: string;
  scale?: number;
}

const EvolvingCharacter = ({ selectionCount, color, scale = 1.4 }: EvolvingCharacterProps) => {
  const stageIndex = getStageIndex(selectionCount);
  const hslColor = `hsl(${color})`;
  const currentSize = STAGE_SIZES[stageIndex];

  return (
    <div
      className="relative flex items-center justify-center"
      style={{
        width: currentSize.width * scale,
        height: currentSize.height * scale,
        transition: "width 0.6s ease-out, height 0.6s ease-out",
        overflow: "visible",
      }}
    >
      {STAGES.map((StageComponent, i) => (
        <motion.div
          key={i}
          className="absolute inset-0"
          initial={false}
          animate={{ opacity: i === stageIndex ? 1 : 0 }}
          transition={{ duration: 0.5, ease: "easeInOut" }}
          style={{ pointerEvents: i === stageIndex ? "auto" : "none" }}
        >
          <StageComponent color={hslColor} />
        </motion.div>
      ))}

      {/* Glow underneath */}
      <motion.div
        className="absolute -bottom-4 left-1/2 -translate-x-1/2 rounded-full blur-xl"
        style={{
          width: 60,
          height: 12,
          backgroundColor: `hsl(${color} / 0.15)`,
        }}
        animate={{ opacity: [0.3, 0.6, 0.3] }}
        transition={{ duration: 3, repeat: Infinity }}
      />
    </div>
  );
};

export default EvolvingCharacter;
